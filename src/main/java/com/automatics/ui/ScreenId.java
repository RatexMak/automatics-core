package com.automatics.ui;

/**
 * Enumeration class to holds the screen id.
 * 
 * @author Selvaraj Mariyappan
 */
public enum ScreenId {

    /** Screen id of main menu screen. */
    MAINMENU,
    
    /** Screen id of settings screen. */
    SETTINGS,

    /** Screen id of help screen. */
    HELP,

    /** Screen id of about screen. */
    ABOUT,

    /** Screen id of on demand screen. */
    ONDEMAND,

    /** Screen id of search screen. */
    SEARCH,

    /** Screen id of guide screen. */
    GUIDE,
    /** Screen id of Mini Guide screen. */
    MINIGUIDE,

    /** Screen id of device settings screen. */
    DEVICESETTINGS,

    /** Screen id of parental screen. */
    PARENTALCONTROL,

    /** Screen id of preference screen. */
    PREFEENCE,

    /** Screen id of Program information screen. */
    PROGRAMINFOSCREEN,

    /** Screen id of Apps Screen screen. */
    APPS

}
